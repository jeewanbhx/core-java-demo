package day4.basic;

public class LiteralsDemo {
    //Literals: a source code representaiton of fixed value
    //Literals: fixed value + data type
    // 1. Integer Literal Type  436 4524 1542454845 45421366
    byte b = 127;
    short s = 32767;
    int i = 2147483647;
    long l = 2147483647;
    long l2 = 21474836478L;


    //2. floating literal 23.24 67.89 (double)

    float f = 32.45f;
    double d = 32.45;

    //3.boolean literals true or false

    boolean bl1 = true;
    boolean bl2 = false;

    // 4. character literals '1' 'a' '@'

    char ch1 = 'a';
    char ch2 = '1';
    char ch3 = '@';
    char ch4 = '\u0000';

    //5. string literals: "fsd"
    String s1 = "hello";
    String s2 = "abc@123";
    String s3 = "123";
    String s4 = "23.45";
    String s5 = "true";
    String s6 = "";
    String s7 = " ";
    String s8 = "Hello,  I am Jeewan B. I am from Arizona USA. I am a Full Stack Java Developer";
    String s9 = "a";

    //6. Null Literals:
    String s10 = null;

    LiteralsDemo ob = new LiteralsDemo();
    LiteralsDemo obj = null;


}
